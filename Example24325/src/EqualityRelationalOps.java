public class EqualityRelationalOps {
    public static void main(String[] args) {
    /// == Equal to signs , and not Equal to != signs

    int Num1 =45;
    int Num2 = 30;

        System.out.println(Num1==Num2); // this will print False

        System.out.println(Num1 + "Is not Equal to " + Num2 + "?"); // prints out a question of equality.

        System.out.println(Num1!=Num2); // This will print True

        //Relation Operators
        System.out.println("Is "+ Num1 + " Is Greater than "+ Num2 +"?");
        System.out.println(Num1>Num2);

        System.out.println("Is "+ Num1 + " Is Less than "+ Num2 +"?");
        System.out.println(Num1<Num2);

        System.out.println("Is "+ Num1 + " Is greater than or Less than  "+ Num2 +"?");
        System.out.println(Num1>=Num2);

        System.out.println("Is "+ Num1 + " Is Less than or Equal to "+ Num2 +"?");
        System.out.println(Num1<=Num2);


    }
}
